    private void BtnDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDisplayActionPerformed
        // TODO add your handling code here:
        
        
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/java_rst_db?useSSL=false","root","");
            Statement st = con.createStatement();
            String sql = "select id,username from users ";
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                String id = String.valueOf(rs.getInt("id"));
                String username = rs.getString("username");
                
                
                String user[] = {id,username};
                DefaultTableModel model = (DefaultTableModel)UserTbl.getModel();
                
                model.addRow(user);
            }
            
            con.close();
            
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_BtnDisplayActionPerformed

    private void BtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)UserTbl.getModel();
        if(UserTbl.getSelectedRowCount()==1){
            model.removeRow(UserTbl.getSelectedRowCount());
        }else{
            if(UserTbl.getRowCount()==0){
                JOptionPane.showMessageDialog(this,"Table is empty");
            }else{
               
                JOptionPane.showMessageDialog(this,"Select a row to delete.");
            }
        }
        
        
    }//GEN-LAST:event_BtnDeleteActionPerformed

